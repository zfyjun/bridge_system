package bridge;

public class SuperStructure {
    private String ssiName;
    private String ssiNo;
    private String sddName;
    private String sddNo;
    private double score;
    private String sdLevel;
    //  private String damageType;
    private double weight;

    public String getSsiName() {
        return ssiName;
    }

    public void setSsiName(String ssiName) {
        this.ssiName = ssiName;
    }

    public String getSsiNo() {
        return ssiNo;
    }

    public void setSsiNo(String ssiNo) {
        this.ssiNo = ssiNo;
    }

    public String getSddName() {
        return sddName;
    }

    public void setSddName(String sddName) {
        this.sddName = sddName;
    }

    public String getSddNo() {
        return sddNo;
    }

    public void setSddNo(String sddNo) {
        this.sddNo = sddNo;
    }

    public double getScore() {
        return score;
    }

    public void setScore(double score) {
        this.score = score;
    }

    public String getSdLevel() {
        return sdLevel;
    }

    public void setSdLevel(String sdLevel) {
        this.sdLevel = sdLevel;
    }

    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }
}
